export function getMartialSoulTier(name: string): number {
  const base = name.replace(/[（(][^)）]*[)）]/g, '').trim()

  if (T1.has(base) || T1.has(name)) return 1
  if (T2.has(base) || T2.has(name)) return 2
  if (T3.has(base) || T3.has(name)) return 3
  if (T4.has(base) || T4.has(name)) return 4
  if (T5.has(base) || T5.has(name)) return 5

  // Tier 6: 极致武魂 prefix + 修罗神剑
  if (/^极[^\s·]+·/.test(base)) return 6
  if (base === '修罗神剑') return 6

  // Concept-type: most are tier 5, rest tier 3-4
  if (/存在定义|力之法则|幻想杀手|无下限术式|轮回法则|不义游戏/.test(base)) return 5
  if (/死而替生|时间暂停|时间加速|春秋蝉|永生者|虚化|斩击/.test(base)) return 4

  // Fallback: common patterns
  if (/王$/.test(base)) return 4
  if (/[龙虎豹狼熊狮鹰蛇]$/.test(base)) return 4

  return 3
}

export function highestMartialSoulTier(context: GameContext): number {
  if (!context.martialSouls.length) return 3
  let highest = 1
  for (const name of context.martialSouls) {
    const tier = getMartialSoulTier(name)
    if (tier > highest) highest = tier
  }
  return highest
}
